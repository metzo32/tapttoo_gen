function getRandomSkills(): string[] {
  let result: string[] = [];
  while (result.length < 4) {
    const randomSkills =
      skills[Math.floor(Math.random() * skills.length)];
    if (!result.includes(randomSkills)) {
      result.push(randomSkills);
    }
  }
  return result;
}
